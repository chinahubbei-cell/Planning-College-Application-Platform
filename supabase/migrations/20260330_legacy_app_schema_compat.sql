create or replace function public.sync_major_subcategory_columns()
returns trigger as $$
begin
  if new.subcategory is null then
    new.subcategory := new.sub_category;
  end if;

  if new.sub_category is null then
    new.sub_category := new.subcategory;
  end if;

  return new;
end;
$$ language plpgsql;

