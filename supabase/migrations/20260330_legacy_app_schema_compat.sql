create or replace function public.get_university_stats()
returns jsonb
language sql
stable
as $$
  with type_stats as (
    select type as key, count(*)::int as value
    from public.universities
    where coalesce(type, '') <> ''
    group by type
  ),
  level_stats as (
    select '985'::text as key, count(*)::int as value
    from public.universities
    where is_985
    union all
    select '211'::text as key, count(*)::int as value
    from public.universities
    where not is_985 and is_211
    union all
    select '双一流'::text as key, count(*)::int as value
    from public.universities
    where not is_985 and not is_211 and is_double_first_class
    union all
    select '普通本科'::text as key, count(*)::int as value
    from public.universities
    where not is_985 and not is_211 and not is_double_first_class
  ),
  province_stats as (
    select province as key, count(*)::int as value
    from public.universities
    where coalesce(province, '') <> ''
    group by province
  )
  select jsonb_build_object(
    'total', (select count(*)::int from public.universities),
    'byType', coalesce((select jsonb_object_agg(key, value) from type_stats), '{}'::jsonb),
    'byLevel', coalesce((select jsonb_object_agg(key, value) from level_stats), '{}'::jsonb),
    'by985', coalesce((select count(*)::int from public.universities where is_985), 0),
    'byProvince', coalesce((select jsonb_object_agg(key, value) from province_stats), '{}'::jsonb)
  );
$$;

create or replace function public.get_score_stats(p_province text default null)
returns jsonb
language sql
stable
as $$
  with filtered as (
    select *
    from public.admission_scores
    where p_province is null or province = p_province
  ),
  bucketed as (
    select
      case
        when min_score is null then '未知'
        when min_score < 400 then '400以下'
        when min_score < 450 then '400-449'
        when min_score < 500 then '450-499'
        when min_score < 550 then '500-549'
        when min_score < 600 then '550-599'
        else '600及以上'
      end as bucket
    from filtered
  ),
  distribution as (
    select bucket as key, count(*)::int as value
    from bucketed
    group by bucket
  ),
  year_trend as (
    select
      year,
      round(avg(min_score)::numeric, 2) as avg_min_score
    from filtered
    where year is not null
      and min_score is not null
    group by year
    order by year
  )
  select jsonb_build_object(
    'distribution', coalesce((select jsonb_object_agg(key, value) from distribution), '{}'::jsonb),
    'yearTrend', coalesce((
      select jsonb_agg(
        jsonb_build_object('year', year, 'avgMinScore', avg_min_score)
        order by year
      )
      from year_trend
    ), '[]'::jsonb),
    'totalRecords', (select count(*)::int from filtered)
  );
$$;

create or replace function public.get_major_stats()
returns jsonb
language sql
stable
as $$
  with category_stats as (
    select category as key, count(*)::int as value
    from public.majors
    where coalesce(category, '') <> ''
    group by category
  )
  select jsonb_build_object(
    'total', (select count(*)::int from public.majors),
    'byCategory', coalesce((select jsonb_object_agg(key, value) from category_stats), '{}'::jsonb)
  );
$$;

create or replace function public.get_distinct_major_categories()
returns table(category text)
language sql
stable
as $$
  select distinct m.category
  from public.majors m
  where coalesce(m.category, '') <> ''
  order by m.category;
$$;

